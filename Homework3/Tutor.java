
public class Tutor {
    
    private String name, ic, address, qualification, dateJoined;
    private int numYearsExp, numYearsCentre;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getIc() {
        return ic;
    }
    public void setIc(String ic) {
        this.ic = ic;
    }
    public String getAddress() {
        return address;
    }
    public void setAddress(String address) {
        this.address = address;
    }
    public String getQualification() {
        return qualification;
    }
    public void setQualification(String qualification) {
        this.qualification = qualification;
    }
    public String getDateJoined() {
        return dateJoined;
    }
    public void setDateJoined(String dateJoined) {
        this.dateJoined = dateJoined;
    }
    public int getNumYearsExp() {
        return numYearsExp;
    }
    public void setNumYearsExp(int numYearsExp) {
        this.numYearsExp = numYearsExp;
    }
    public int getNumYearsCentre() {
        return numYearsCentre;
    }
    public void setNumYearsCentre(int numYearsCentre) {
        this.numYearsCentre = numYearsCentre;
    }

    

}
